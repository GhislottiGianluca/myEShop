package com.example.myEShop.appuser;

/**
 * Enumeration representing the roles of an application user.
 * <p>
 * This enum defines the different roles that a user can have within the application.
 * </p>
 */
public enum AppUserRole {

    /**
     * Standard user role with basic permissions.
     */
    USER,

    /**
     * Administrative user role with elevated permissions.
     */
    ADMIN
}
